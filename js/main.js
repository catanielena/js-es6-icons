const printIcons = (arr, container) => {
    arr.forEach(
        (e) => {
            container.innerHTML += `
            <div class="icons-wrapper__card">
               <i class="card__icon ${e.family} ${e.prefix + e.name}" style="color:${e.color}"></i>
               <div class="card__label">
                    ${e.name}
               </div>
            </div>`;
        }
    );
};
// array icons and colors
